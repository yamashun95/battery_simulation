import numpy as np


def ocv_LMO(y):
    # if y >= 1:
    #    y = 0.9999999  # 1未満の最大値に設定
    return np.real(
        4.19829
        + 0.0565661 * np.tanh(-14.5546 * y + 8.60942)
        - 0.0275479 * ((0.9984322 - y) ** (-0.492465) - 1.90111)
        - 0.157123 * np.exp(-0.04738 * y**8)
        + 0.810239 * np.exp(-40 * y - 0.133875)
    )
